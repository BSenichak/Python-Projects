"""
Задача 3: Пошук найбільшого числа
Запросити користувача ввести послідовність чисел,
в якій кількість елементів відома заздалегідь.
Знайти та вивести найбільше число з цієї послідовності.
"""

count = int(input("Скільки буде чисел: "))
max_num = float(-99999999)
for i in range(count):
    num = float(input("Введіть своє число: "))
    if num > max_num:
        max_num = num
print(f'Найбільше число {max_num}')